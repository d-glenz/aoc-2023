import tqdm
import functools
from dataclasses import dataclass 

@dataclass 
class Obj:
    x: int
    y: int
    id: int
    ch: str

class Field:
   def __init__(self, b):
       self.data=[list(r) for r in b.splitlines()]
       round=[(x, y) for y, r in enumerate(self.data) for x, c in enumerate(r) if c=='O']
       square=[(x, y) for y, r in enumerate(self.data) for x, c in enumerate(r) if c=='#']
       self.objects={}
       self.by_coord={}
       self.height=len(self.data)
       self.width=len(self.data[0])
       
       i=0
       for x, y in round:
           o=Obj(x, y, i, 'O')
           self.objects[i]=o
           self.by_coord[(x,y)]=o
           i+=1
       for x, y in square:
           o=Obj(x, y, i, '#')
           self.objects[i]=o
           self.by_coord[(x, y)]=o
           i+=1
           
   def __getitem__(self, k):
       if isinstance(k, tuple):
           x, y = k
           if isinstance(x, slice) or isinstance(y, slice):
               lst=self.objects.values()
               if isinstance(x, slice):
                   x=slice(0 if x.start is None else x.start, 
                           self.width if x.stop is None else x.stop)
                   lst=[o for o in lst if o.x>=x.start and o.x<x.stop]
               if isinstance(y, slice):
                   y=slice(0 if y.start is None else y.start, 
                           self.width if y.stop is None else y.stop)
                   lst=[o for o in lst if o.y>=y.start and o.y<y.stop]
               return lst
           if x is None:
               return [o for _, o in self.objects.items() if o.y==y]
           if y is None:
               return [o for _, o in self.objects.items() if o.x==x]
           return self.by_coord[k]
       return self.objects[k]
       
   def __setitem__(self, k, v):
       if isinstance(k, tuple):
           o=self.by_coord[k]
           self.objects[o.id].x=v[0]
           self.objects[o.id].y=v[1]
           del self.by_coord[k]
           self.by_coord[v]=o
           return
       o=self.objects[k]
       del self.by_coord[(o.x,o.y)]
       self.objects[k].x=v[0]
       self.objects[k].y=v[1]
       self.by_coord[v]=o
       
   def __repr__(self):
       b=[] 
       for y in range(self.height):
           t=[]
           for x in range(self.width):
               if (x, y) in self.by_coord:
                   t.append(self.by_coord[(x,y)].ch)
               else:
                   t.append('.')
           b.append(t)
       return '\n'.join([''.join(r) for r in b])


